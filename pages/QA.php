                    Классификация тестов по приложению
                </h1>
                <h2>Виды тестирования</h2>
                <br/>
                <b>Функциональные тесты (Functional testing)</b>
                <ul>
                    <li>Забронировать комнату</li>
                    <li>Проверить Поиск в разделе Новости</li>
                </ul>
                <br/>
                <b>Тесты производительности (Performance testing)</b>
                <ul>
                    <li>Определить количество пользователей, одновременно работающих с приложением</li>
                </ul>
                <br/>
                <ul>Нагрузочные тесты (Load testing)
                    <li>Сымитировать вход на сайт одновременно 5000 пользователей</li>
                </ul>
                <br/>
                <b>Тестирование надежности (Reliability testing)</b>
                <ul>
                    Проверить, что сайт может работать под нагрузкой (посещение сайта меняется от 0 до 50 000
                    посетителей) 5 дней подряд.
                </ul>
                <br/>
                <b>Тестирование совместимости (Compatibility testing)</b>
                <ul>
                    <li>Проверить работу сайта на разных браузерах Google Chrome, Edge, Safari, Opera, Mozilla Firefox</li>
                    <li>Проверить работу сайта на разных устройствах мобильное устройство (Android, IOS), Ноутбук,
                        стационарный компьютер, планшет</li>
                </ul>
