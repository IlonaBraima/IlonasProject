                    <h1 id="moneytour">Исследовательское тестирование: Денежный тур (Money Tour) Уиттакера на Playday.lv</h1>
                    <p>
                        Для своего проекта я выбрала Денежный тур, потому что это напрямую вяжется с целю создания сайта,
                        где можно посмотреть и выбрать себе место для любого детского праздника.
                    </p>
                    <p>
                        <b>Цель тура:</b> найти функции, которые заставляют людей тратить деньги на ваше ПО и проверить

                        их работу.
                    </p>
                    <p>
                        <b>Время тура:</b> 20 минут.
                    </p>
                    <p>
                        <b>В путь!</b>
                    </p>
                    <p>
                        Итак, нажав кнопку «О нас» мы перенесёмся в мини рассказ о том, что вас ждет в Playday.lv.
                    </p>
                    <img src="../images/qaimages/moneytour/1.png" alt="Иллюстрация" width="100%">
                    <p>
                        С первых строк понятно, что здесь можно просто прийти поиграть, побегать, попрыгать,
                        устроить частный детский праздник, посетить занятие в “Playschool” или записать ребёнка
                        в дневной лагерь на каникулах.
                    </p>
                    <img src="../images/qaimages/moneytour/2.png" alt="Иллюстрация" width="100%">
                    <p>
                        Листая дальше, мы сразу видим, какие услуги нам может предложить Playday.lv.
                        Но здесь они показаны частично (нет даже и трети из всего разнообразия), но об этом
                        мы узнаем только тогда, когда пройдём непосредственно по другим разделам.
                    </p>
                    <img src="../images/qaimages/moneytour/3.png" alt="Иллюстрация" width="100%">
                    <p>
                        Изучая далее, мы узнаём информацию, что Playday.lv устраивает массовые семейные мероприятия,
                        как на улице, так и у себя в помещениях.
                    </p>
                    <img src="../images/qaimages/moneytour/4.png" alt="Иллюстрация" width="100%">
                    <p>
                        Переходим по кнопке «Предстоящие мероприятия» понимаем, что ссылка битая. Во-первых,
                        сразу переключает тебя на латышский язык, а информация не отображается.
                    </p>
                    <img src="../images/qaimages/moneytour/5.png" alt="Иллюстрация" width="100%">
                    <p>
                        Идём смотреть, что же мы можем зарезервировать. В выплывающем списке можем видеть
                        пять игровых комнат. Названия комнат то написаны с маленькой буквы, то с большой.
                        Смотрится некрасиво.
                    </p>
                    <img src="../images/qaimages/moneytour/6.png" alt="Иллюстрация" width="100%">
                    <p>
                        В окне резервации опять появляется информация о центре.
                    </p>
                    <img src="../images/qaimages/moneytour/7.png" alt="Иллюстрация" width="100%">
                    <p>
                        Если хочется посмотреть больше фото, то нужно нажать на кнопку “Больше фотографий”.
                    </p>
                    <img src="../images/qaimages/moneytour/8.png" alt="Иллюстрация" width="100%">
                    <p>
                        После нажатия на кнопку появляется только ТРИ новые фотографии.
                    </p>
                    <img src="../images/qaimages/moneytour/9.png" alt="Иллюстрация" width="100%">
                    <p>
                        После повторного нажатия, криво появляется ещё три.
                    </p>
                    <img src="../images/qaimages/moneytour/10.png" alt="Иллюстрация" width="100%">
                    <p>
                        Нажимаем ещё раз и появляется последняя тройка и всё!
                    </p>
                    <img src="../images/qaimages/moneytour/11.png" alt="Иллюстрация" width="100%">
                    <p>
                        Пробегаем по комнатам. Комнаты отличаются вместимостью от 12 до 25 человек,
                        цветом стен и мебели, остальное в них стандарт – столы, стулья, вода, микроволновка,
                        чайник, пресс для кофе, графины.
                    </p>
                    <img src="../images/qaimages/moneytour/12.png" alt="Иллюстрация" width="100%">
                    <p>
                        В каждой комнате тоже можно посмотреть фото, но очень ограничено - 3-4 фотографии.
                    </p>
                    <img src="../images/qaimages/moneytour/13.png" alt="Иллюстрация" width="100%">
                    <p>
                        В резервации же можно почитать отзывы родителей.
                    </p>
                    <img src="../images/qaimages/moneytour/14.png" alt="Иллюстрация" width="100%">
                    <p>
                        Далее в меню “Услуги” кнопка не нажимается, но в выпадашке дублируются “Услуги”,
                        нажимая на которые и выходим на общую страницу услуг. Здесь, скорее всего, забыли
                        перевести на русский Services – услуги.
                    </p>
                    <img src="../images/qaimages/moneytour/15.png" alt="Иллюстрация" width="100%">
                    <p>
                        Дополнительные к праздникам услуги сразу идут с расценками.
                    </p>
                    <img src="../images/qaimages/moneytour/16.png" alt="Иллюстрация" width="100%">
                    <p>
                        Почему-то цену не поставили к Диско вечеринке, явно пропустили.
                    </p>
                    <img src="../images/qaimages/moneytour/17.png" alt="Иллюстрация" width="100%">
                    <p>
                        Но нажав на активное название, все цены и описание есть. Но описание зачем-то два раза.
                    </p>
                    <img src="../images/qaimages/moneytour/18.png" alt="Иллюстрация" width="100%">
                    <p>
                        Под описанием любой услуги, можно посмотреть небольшой перечень других услуг, перейти
                        в которые можно нажав или на активное фото или название.
                    </p>
                    <img src="../images/qaimages/moneytour/19.png" alt="Иллюстрация" width="100%">
                    <p>
                        То, что всё здесь покупается и продается свидетельствует Корзина (значок), который
                        не меняет своего месторасположения, как бы мы не крутили курсором вверх или вниз.
                    </p>
                    <img src="../images/qaimages/moneytour/20.png" alt="Иллюстрация" width="100%">
                    <p>
                        Помимо того, что корзина всегда с нами) В Разделе меню она же тоже есть.
                    </p>
                    <img src="../images/qaimages/moneytour/21.png" alt="Иллюстрация" width="100%">
                    <p>
                        Чтобы туда положить всё желаемое, нет необходимости регистрироваться заранее.
                        Заполняемся все поля только при оформлении заказа. Опять же оформление заказа,
                        как и все активные кнопки доступны на латышском языке.
                    </p>
                    <img class="litlleimg" src="../images/qaimages/moneytour/22.png" alt="Иллюстрация">
                    <p>
                        За последнюю минуту тура, ещё можно успеть пробежать по ценам. Цены вставлены
                        в виде картинки и сразу кидается в глаза, что картинка маленького разрешения.
                    </p>
                    <img class="mediumimg" src="../images/qaimages/moneytour/23.png" alt="Иллюстрация">
                    <p>
                        Выводы: подводя итоги тура можно сразу отметить, что тур помог по другому посмотреть на сайт.
                        За эти 20 минут нашлось много недочетов, недоделок сайта.
                        <br/>
                        1) Есть битые ссылки и даже целые страницы неотображаются.
                        <br/>
                        2) У некоторых услуг не проставлены цены.
                        <br/>
                        3) У основных продуктов на продажу мало визуальной информации, картинки с ценами плохого разрешения.
                        <br/>
                        4) При переключении функций, прыгают языки.
                        <br/>
                        5) Требуется поработать над тем, чтобы кнопки главного меню приобрели единый стиль и функциональность.
                    </p>
